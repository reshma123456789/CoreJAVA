package january19;

public class SwitchExample2 {
	public static void main(String[] a) {
		
		String day="Wednesday";
		switch(day) {
		case "Sunday":System.out.println("Day 1");
		break;
		case "Monday":System.out.println("Day 2");
		break;
		case "Tuesday":System.out.println("Day 3");
		break;
		case "Wednesday":System.out.println("Day 4");
		break;
		case "Thursday":System.out.println("Day 5");
		break;
		case "Friday":System.out.println("Day 6");
		break;
		case "Saturday":System.out.println("Day 7");
		break;
		default:System.out.println("Invalid");
		
		}
				
	}

}
